# Comentário de uma só  linha
'''
    Comentário de
        muuuuuuultiplas
         linhas........
'''

# Boolean
verdadeiro = True
falso = False
print(verdadeiro, falso)

# Int
inteiro = 1
print(inteiro)

# Float
ponto_flutuante = 1.0
print(ponto_flutuante)

# String
saudacao = 'Hello World!'
print(saudacao)

# None
sem_valor = None
print(sem_valor)

# Dict
pessoa = {'nome': 'David Sotto Mayor', 'idade': 21,
          'universitario': True, 'expertise': ['matemática', 'computação']}
print(pessoa)

# List
misturado = ['David', 21, True, ['matemática', 'computação']]
print(misturado)

# Tuple
cidade_estado = ('Manaus', 'AM')
print(cidade_estado)

# List + Dict
compras = [
    {'produto': 'tomate', 'quantidade': 3, 'preco_unitario': 8.9},
    {'produto': 'arroz', 'quantidade': 1, 'preco_unitario': 3.49}
]
print(compras)
print(type(compras))
